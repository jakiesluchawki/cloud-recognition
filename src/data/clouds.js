export const cloudLevels = ["wszystkie", "niskie", "średnie", "wysokie", "pionowe", "wielopoziomowe"];

export function getCloud(id) {
  return clouds.find((cloud) => cloud.id === id);
}

export function getCloudImage(cloudOrId, imageId) {
  const cloud = typeof cloudOrId === "string" ? getCloud(cloudOrId) : cloudOrId;
  return cloud?.images.find((image) => image.id === imageId) || cloud?.images[0];
}
